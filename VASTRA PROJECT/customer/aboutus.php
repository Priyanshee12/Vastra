<h2><b>OUR STORY</b></h2>

We are a fun-loving and dedicated group of people who have come together with a unified passion to make a difference in the world. 
Our story is one of calling, courage, and collaboration. 
We choose to use our gifts and talents to see broken lives renewed, the oppressed set free and people educated on the power of their buying choices.
 Our story is unlikely, but we’re passionate, driven and just crazy enough to actually make a difference.
<br>
<h2><b>CALLING</b></h2>

Our founder Christie Johnson, dreamt of combining her gift for style with a greater Purpose. 
She dreamt of opening a boutique focused on not just styling but loving, serving, and giving generously to people in need. 
For years she prayed about this dream, asking God for His perfect timing and to use her gifts and talents to see lives healed around the world. 
<br>
<h2><b>COURAGE</b></h2>

In a powerful answer to prayer, Christie’s calling became clear in the summer of 2013 - now was the time to open the boutique she dreamt about. 
Moving forward required tremendous faith and obedience to face the fear of failure. Thousands of miles from friends and family, this was an unlikely time and an unlikely place-as a Navy wife with two babies in Bremerton, WA.
 But Christie knew that Purpose would become immeasurably more than all she could ask for or imagine. She knew that God spoke Purpose into existence and that it would take a team to build it.
 Together with her husband, Flip, Christie launched the first Purpose Boutique and immediately watched it grow faster than anyone thought possible.
<br>
<h2><b>COLLABORATION</b></h2>
Soon more team members joined, bringing diverse skill sets and passions, unified by a shared Purpose. 
As the business grew, so did the vision. The team realized that Purpose could take loving people through styling and donating to another level.
 Purpose had been called to give, grow business, shop, and manufacture, differently.
 So we started researching and collaborating with a handful of brands and businesses with a similar calling. These brands empower disadvantaged people with holistic support and provide them with dignified work producing beautiful jewelry and accessories. 
We now carry their products in our stores and proudly call them our “empowerment partners.” 
While empowerment lines now make up the majority of our jewelry and accessories, we noticed a lack of clothing brands that were cute, affordable AND empowering the oppressed and exploited who create them.
 So we decided to start our own! We’re now collaborating and partnering with multiple empowerment manufacturers to produce an adorable, affordable Purpose clothing line made with hope and dignity by survivors of injustice. 

As our story continues to unfold, we are powered by a faith that we all play a part in making this vision becoming reality.
 And we hold tight to the simple truth that how you shop can truly change a life.
